def build_enemy_prompt(play_log):
    return f"""
    너는 TRPG 게임의 마스터이며 지금까지 진행된 게임 로그를 기반으로 적을 생성해야한다.
지금까지 게임이 진행되면서 만들어진 게임 로그는 이러하다:
'{play_log}'

이것을 기반으로 아래 정보를 생성하라:
0. 모든 value 값은 한글로 표기한다. 플레이 로그는 상황을 만들기 위한 기반 자료일 뿐 해당 내용이 직접적으로 들어가면 안된다. 그리고 내용이 완전하게 중복되어선 안된다.
1. 마주칠 적 생성 (설명, 능력치, 패턴 포함) 적은 플레이어와 1대1 구도를 상정하여 다수를 노리는 패턴은 만들지 않는다.
2. 적의 패턴은 최소 2개 이상 5개 이하로 설정한다.
3. 적의 패턴은 attack, strength로 구분한다
4. attack은 hp(체력)와 wp(정신력) 둘 하나만 공격하며,  cf 값을 1.0~1.5로 패턴이름과 어울리게 설정한다.
5. strength는 hp(체력)와 atk(공격력) 둘중 하나만 hp- >회복, atk -> 강화하며, cf 값을 5~10으로 패턴이름과 어울리게 설정한다.
6. 게임 로그를 기반으로 현 상황에 어울리는 적을 생성한다.
7. ```json ``` 텍스트는 삭제, '은 사용하지 않는다.
8. 적의 hp는 20 ~ 30 사이로 설정 atk는 5~12로 설정
7. 작은따옴표(')는 사용하지 않고, 모든 key와 문자열 value는 반드시 큰따옴표(")로 감싼다.
9. 해당 이벤트를 평문으로 log['exp']:value에 저장 

반드시 JSON 형식으로 다음과 같이 반환하라: 
{{
"enemy": {{
    "name": "...",
    "species": "...",
    "hp": ...,
    "atk": ...,
    "description": "...",
    "special_patterns": [{{
        "name": "...",
        "type": {{
            "kind" :"strength",
            "stat": "...",
            }},
        "cf": ...,
    }},{{ 
        "name": "...",
        "type": {{
            "kind" : "attack",
            "stat": "...",
        }},
        "cf": ...,
    }},{{...}}]
  }}
"log": {{
    "type":"create_enemy",
    "exp":"..."
    }}
}}

"""