def build_enemy_prompt(play_log):
    return f"""
    너는 TRPG 게임의 마스터이며 지금까지 진행된 게임 로그를 기반으로 적을 생성해야한다.
지금까지 게임이 진행되면서 만들어진 게임 로그는 이러하다:
'{play_log}'

이것을 기반으로 아래 정보를 생성하라:
0. 모든 value 값은 한글로 표기한다.
1. 마주칠 적 생성 (설명, 능력치, 패턴 포함) 적은 플레이어와 1대1 구도를 상정하여 다수를 노리는 패턴은 만들지 않는다.
2. 게임 로그를 기반으로 현 상황에 어울리는 적을 생성한다.
3. ```json ``` 텍스트는 삭제
9. 해당 이벤트를 평문으로 log['exp']:value에 저장 

JSON 형식으로 다음과 같이 반환하라:
{{
"enemy": {{
    "name": "...",
    "species": "...",
    "hp": ...,
    "atk": ...,
    "description": "...",
    "special_patterns": [...]
  }}
"log: {{
    "type":"create_enemy",
    "exp":"..."
    }}
}}

"""
