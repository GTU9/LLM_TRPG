def build_event_prompt_before(play_log):
    return f"""
너는 TRPG 게임의 마스터이며 지금까지 진행된 게임 로그를 기반으로 게임 랜덤 인카운터를 생성해야한다.
지금까지 게임이 진행되면서 만들어진 play_log는 이러하다:
{play_log}

이것을 기반으로 아래 정보를 생성하라:
0. 문자열 value 값은 한글로 표기한다.
1. 플레이 로그는 상황을 만들기 위한 기반 자료일 뿐 해당 내용이 직접적으로 들어가면 안된다. 내용이 완전하게 중복되어선 안된다.
2. [플레이어 이동/의도]가 있다면 해당 방향과 의도를 최우선으로 반영하여 상황을 생성하라.
3. 플레이어에게 지금 처해진 상황을 상세하고 자세하게 설명한다.
4. 전투 이벤트가 아닌 단순한 상황에서 플레이어가 행동을 통해 스탯에 영향을 준다.
5. 게임 로그를 기반으로 현 상황에 어울리는 상황을 생성한다.
6. 수치는 묘사하지 않는다.
7. 상황 묘사 마지막에 플레이어가 어떻게 행동할지 자연스럽게 유도하는 질문을 덧붙여라. 질문은 지금까지의 스토리 흐름과 현재 상황을 반영하여 구체적으로 작성하라. 예) "낡은 설계도를 해독하겠는가, 아니면 다른 단서를 찾아 공방을 뒤지겠는가?"
8. 작은따옴표(')는 절대 사용하지 않는다. 모든 key와 문자열 value는 반드시 큰따옴표(")로 감싼다.
9. ```json``` 텍스트는 포함하지 않는다.

반드시 유효한 JSON 형식으로 다음과 같이 반환하라:
{{
    "event": {{
        "name": "...",
        "explain": "..."
    }},
    "log": {{
        "type": "event_before",
        "exp": "..."
    }}
}}
"""


def build_event_prompt_after(event_log, player_act, roll_info):
    return f"""
너는 TRPG 게임의 마스터이며 직전 이벤트에서 플레이어가 한 행동과 주사위 판정 결과를 기반으로 플레이어의 스탯에 변화를 주는 이벤트를 진행한다.
직전 이벤트: {event_log}
플레이어의 행동: {player_act}
주사위 판정 결과: {roll_info}

이것을 기반으로 아래 정보를 생성하라:
0. 문자열 value 값은 한글로 표기한다. 숫자 value는 반드시 정수(integer)로 표기한다. "감소", "증가" 같은 한글 문자열을 숫자 대신 사용하면 절대 안 된다.
1. 플레이어의 반응에 대한 피드백을 상세하고 자세하게 설명한다.
2. 전투 이벤트가 아닌 단순한 상황에서 플레이어가 행동을 통해 스탯에 영향을 준다.
3. 행동으로 인해 발생한 결과를 자세하게 설명한다.
4. 플레이어 능력치: hp(체력), wp(정신력), str_(힘), dex_(민첩), int_(지능), char_(화술). 결과에 따라 증가(양수 정수) 또는 감소(음수 정수)로 표기한다.
5. 변화량이 0이면 type에 작성하지 않는다.
6. 주사위 등급에 따라 아래 규칙을 반드시 따라라:
   - 대실패: hp 또는 wp 반드시 감소(-18 ~ -20), 능력치 향상 없음, 추가 패널티 가능
   - 실패: hp 또는 wp 감소(-15 ~ -18), 능력치 향상 최대 +1
   - 보통: hp/wp 소폭 감소(-15) 또는 소폭 향상(+15), 능력치 ±1 가능
   - 성공: hp/wp 향상(+15 ~ +20) 위주, 능력치 +1~+2 가능
   - 대성공: hp/wp 향상(+20 ~ +25), 능력치 +2~+3 가능
   - 완벽한 성공: hp/wp 반드시 향상(+25 ~ +30), 능력치 +3 가능, 추가 보너스 가능
7. 수치는 묘사하지 않는다.
8. 해당 능력치가 왜 변화했는지 묘사에 녹여낸다.
9. 결과 묘사 마지막에 앞으로 나아간다는 표현을 넣는다.
10. 작은따옴표(')는 절대 사용하지 않는다. 모든 key와 문자열 value는 반드시 큰따옴표(")로 감싼다.
11. ```json``` 텍스트는 포함하지 않는다.

반드시 유효한 JSON 형식으로 다음과 같이 반환하라. type의 value는 반드시 정수여야 한다:
{{
    "event": {{
        "name": "...",
        "explain": "...",
        "type": {{"wp": -15, "str_": 2}}
    }},
    "log": {{
        "type": "event_after",
        "exp": "..."
    }}
}}
"""
